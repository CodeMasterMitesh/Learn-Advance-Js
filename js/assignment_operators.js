// assignment operators
// =,+=,-=,*=,/=

let x = 10;
let y = 3;

x+=y; // x = x + y;
x-=y; // x = x - y;
y*=x; // y = y * x;
y/=x; // y = y / x;

console.log('x = ',x);
console.log('y = ',y);



